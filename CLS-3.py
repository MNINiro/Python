t = True
f = False

print("=========AND=========")
print(f and f)
print(t and f)
print(f and t)
print(t and t)

print("=========OR=========")
print(f or f)
print(t or f)
print(f or t)
print(t or t)

print("=========NOT=========")
print(not t)
print(not f)

print("=========XOR=========")
print(f ^ f)
print(t ^ f)
print(f ^ t)
print(t ^ t)